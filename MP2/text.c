/*
 *   textToScreen
 *   DESCRIPTION: We know the height and width of the status bar, so we use that info
 *                To fill the status bar with background color info. Then we access the
 *                The individual ascii character and the pixel values of  8 x 16 block and
 *                Then find if we should fill in a color for the specific pixel. That is 
 *                How we turn text to Image. 
 *   INPUTS: text_color -- used as different color for the status bar text
 *           background_color -- used for background color. We fill the whole status bar with this first
 *           string -- The string that has to be output on the screen
 *           buffer -- An array. It's used to put in the pixel colors.
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: buffer is full of color values.
 */
 extern void textToScreen(unsigned char text_color, unsigned char background_color, const char* string, unsigned char* buffer)
{
    int i; /* this is going to be used to traverse the string, when we do the for loop */
    int j; /* this is going to be used to traverse the font data columns, when we convert from text to image */
    int k; /* this i going to be used to traverse the pixel columns, when we conver from text to image */
    int total_pixel; /* holds the value of the total number of pixels */ 

    int str_length; /* calculate the length of the string and put it in here */
    int center_index; /* calculate the center of width and store here */
    int current_index; /* temp index, that keeps moving in the for loop */

    int bit_mask = 0x80;

    str_length = strlen(string); /* get the lenght of the string */
    center_index = 320 - (str_length * 8); /* based on that we find the center location using the width and width size of the string */
    center_index = center_index / 2; /* divide it by 2 to normalize it */

    total_pixel  = 320 * 18; /* This is the total amount of pixels in the status bar. We access each of them to change the background */
    for(i = 0; i < total_pixel; i++)
    {
        buffer[i] = background_color; /* We change the background color of the whole status bar */
    }

    for(i = 0; i < str_length; i++) /* Accessing each character in string */
    {
        for(j = 1; j < 18; j++) /* Accesing each column in the font_data */
        {
            bit_mask = 0x80; /* Used to check if we should change the pixel color or not */

            for(k = 0; k < 8; k++) /* Accessing each pixel column */
            {
                current_index = (center_index / 4) + ((k % 4) * 1440) + (j * 80) + (i * 2) + (k / 4); /* calculate where we are in the buffer. Out of the 5760 pixels, where are you? */

                int char_to_ascii = (int)string[i]; /* Convert ascii value to int */
                if(font_data[char_to_ascii][j-1] & bit_mask) /* Use the int value of character to find the specific ascii character in font_data array */
                {                                            /* If we can color the pixel then we change the pixel color at this index */
                    buffer[current_index] = text_color;
                }

                bit_mask >>= 1;                             /* We start with index 7, then check index 6, and so on */
            }
        }
    }

}

